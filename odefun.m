function z=odefun(t,y)
global a12 a23 a32 a34 a24 a42 a25 a56 a65 a67 a57 a75 a58 a89 a98 a9_10 a8_10 a10_8 a8_11

z = zeros(length(y),1);
z(1) = -a12 * y(1); %dx/dt
z(2) = a12*y(1) + a42*y(4) + a32*y(3) - a24*y(2) - a23*y(2) - a25*y(2); %dy/dt
z(3) = a23*y(2) - a32*y(3) - a34*y(3); %dz/dt
z(4) = a24*y(2) + a34*y(3) - a42*y(4); %dk/t
z(5) = a25*y(2) + a75*y(7) + a65*y(6) - a57*y(5) - a56*y(5) - a58*y(5);
z(6) = a56*y(5) - a65*y(6) - a67*y(6);
z(7) = a57*y(5) + a67*y(6) - a75*y(7);
z(8) = a58*y(5) + a98*y(9) + a10_8*y(10) - a89*y(8) - a8_10*y(8) - a8_11*y(8);
z(9) = a89*y(8) - a98*y(9) - a9_10*y(9);
z(10) = a8_10*y(8) + a9_10*y(9) - a10_8*y(10);
z(11) = a8_11*y(8);

end